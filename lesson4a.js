// functions with parameters 
// example 1 
function greeting (name) {
    console.log("Hello" , name);
    
}
// call the function 
greeting("Mike")


greeting ("Bevy")


greeting("John")

// example 2 
function mycounty (county) {
    console.log("My county is", county);
    
}
// call the function 
mycounty ("Nairobi")

mycounty("Naivasha")



// example 3 
// addition 
function addition (num1,num2) {
    answer=num1+num2
    console.log("the sum is" , answer);
    
}
// call the function 
addition(50,100)
addition(800,700)
addition(44,-160)
addition(-90,-180)    

// subtraction 
function subtraction (num1,num2) {
    answer=num1-num2
    console.log("the difference is", answer);
    
}    
// call the function 
subtraction(200,100)
subtraction(140,55)
subtraction(20,90)


// multiplication 
function multiplication (num1,num2) {
    answer=num1*num2
    console.log("the product is", answer);
    
}
// call the function 
multiplication (104,77)
multiplication(55,10.2)
multiplication(33,88)
